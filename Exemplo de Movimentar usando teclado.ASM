;EXEMPLO DE MOVIMENTAR USANDO TECLADO


CLO
	MOV BL, D0
LOOP:
	MOV CL, 23
	MOV [BL], CL
	IN 00
	MOV CL, 20
	MOV [BL], CL
	CMP AL, 57
	JZ SOBE
	CMP AL, 5A
	JZ DESC
	CMP  AL, 41
	JZ ESQ
	CMP AL,  53
	JZ DIR
	JMP LOOP
	DESC:
	ADD BL, 10
	CMP BL, C0
	JS LOOP
	OR BL, C0
	JMP LOOP
SOBE:
	SUB BL, 10
	CMP BL, C0
	JNS LOOP
	OR BL, F0
	JMP LOOP
DIR:
	INC BL
	PUSH BL
	AND BL, 0F
	POP BL
	JNZ LOOP
	SUB BL, 10
	JMP LOOP
ESQ:
	DEC BL
	PUSH BL
	OR BL, F0
	CMP BL, FF
	POP BL
	JNZ LOOP
	ADD BL, 10
	JMP LOOP
	HALT
END